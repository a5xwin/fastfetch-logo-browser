    show_logo
    read -rsn1 key
    if [[ $key == $'\x1b' ]]; then
        read -rsn2 -t 0.1 key2
        case $key2 in
            "[D") # for left arrow key
                ((index--))
                [[ $index -lt 0 ]] && index=$((total - 1))
